package esc.vo;

public class UserStatics {
   private String userNo;
   private double totalReservationTime;
   private int rankReservationTime;
   
   public String getUserNo() {
      return userNo;
   }
   public void setUserNo(String userNo) {
      this.userNo = userNo;
   }
   public double getTotalReservationTime() {
      return totalReservationTime;
   }
   public void setTotalReservationTime(double totalReservationTime) {
      this.totalReservationTime = totalReservationTime;
   }
   public int getRankReservationTime() {
      return rankReservationTime;
   }
   public void setRankReservationTime(int rankReservationTime) {
      this.rankReservationTime = rankReservationTime;
   }
   

}